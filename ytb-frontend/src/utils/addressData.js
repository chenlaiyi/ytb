// 根据省份代码获取城市列表
export function getCitiesByProvince(provinceCode) {
  const province = addressData.provinces.find(p => p.code === provinceCode)
  return province ? province.cities : []
}

// 根据城市代码获取区县列表
export function getDistrictsByCity(provinceCode, cityCode) {
  const province = addressData.provinces.find(p => p.code === provinceCode)
  if (!province) return []
  
  const city = province.cities.find(c => c.code === cityCode)
  return city ? city.districts : []
}

// 根据代码获取名称
export function getNameByCode(type, code) {
  if (type === 'province') {
    const province = addressData.provinces.find(p => p.code === code)
    return province ? province.name : ''
  }
  
  if (type === 'city') {
    for (const province of addressData.provinces) {
      const city = province.cities.find(c => c.code === code)
      if (city) return city.name
    }
    return ''
  }
  
  if (type === 'district') {
    for (const province of addressData.provinces) {
      for (const city of province.cities) {
        const district = city.districts.find(d => d.code === code)
        if (district) return district.name
      }
    }
    return ''
  }
  
  return ''
} 